    //submit quiz - checks answers, display's score
    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        //checks each question
        for (int i = 0; i < curQuiz.getNumQ(); i++) {
            curQuiz.checkQ(i, txtAns[i].getText());
            txtAns[i].setEditable(false);
        }
        
        btnSubmit.setEnabled(false);
        //allow user to review answers
        btnSeeCorrect.setEnabled(true);
        
        //display score
        JOptionPane.showMessageDialog(null, "Score: " + curQuiz.calcScore() + "%");
        
        //resets quiz score stored
        curQuiz.reset();
    }//GEN-LAST:event_btnSubmitActionPerformed

    //closes opened quiz, asks if user wants to save quiz (if not already saved)
    private void btnExitQuizActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExitQuizActionPerformed
        //asks to save quiz or not
        
        if(!curQuiz.isSaved()){
            String[] options = {"Save", "Don't Save"};
            int save = JOptionPane.showOptionDialog(null, "Do you want to save this quiz?", "Save Quiz", JOptionPane.DEFAULT_OPTION, JOptionPane.PLAIN_MESSAGE, null, options, options[0]);
            //user enters title if presses save
            if (save == 0) {
                String title = JOptionPane.showInputDialog("Enter title: ", sub.toString() + (sub.getNumQuizzes() + 1));
                curQuiz.setTitle(title);
                sub.saveQuiz(curQuiz);
                cboQuizzes.addItem(title);
            }
        }
        
        //returns home
        close();
        pnlHome.setVisible(true);
    }//GEN-LAST:event_btnExitQuizActionPerformed

