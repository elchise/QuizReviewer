    //displays quiz for user to take
    private void displayQuiz(Quiz quiz){
        
        //sets/displays each question, clears answer boxes
        for (int i = 0; i < quiz.getNumQ(); i++){
            qLabels[i].setText("<html><p>" + (i+1) + ". " + quiz.getQuestion(i).getQ() + "</p></html>");
            txtAns[i].setText("");
            txtAns[i].setEditable(true);
            qLabels[i].setVisible(true);
            txtAns[i].setVisible(true);
        }
        
        //sets excess questions as not viewable
        if (quiz.getNumQ() < qLabels.length){
            for (int i = quiz.getNumQ(); i < qLabels.length; i++){
                qLabels[i].setVisible(false);
                txtAns[i].setVisible(false);
            }
        }
        
        //displays the quiz
        pnlQuizDisplay.setVisible(true);
        btnSeeCorrect.setEnabled(false);
        cboSub.setEnabled(false);
    }
    
    //closes all panels
    private void close(){
        pnlHome.setVisible(false);
        pnlAddQ.setVisible(false);
        pnlQuizDisplay.setVisible(false);
        pnlReview.setVisible(false);
        pnlSetRev.setVisible(false);
        cboSub.setEnabled(true);
    }
    
