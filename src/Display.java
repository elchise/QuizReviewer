    //sets what the current subject is when combo box selection is changed
    private void cboSubActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cboSubActionPerformed
        if (cboSub.getSelectedItem().equals("New Subject")){
            //creates new subject if correlating combo box item is selected
            String name = JOptionPane.showInputDialog("Enter subject name: ", "Subject" + cboSub.getItemCount());
            if (name != null){
              Subject sub = new Subject(name);
              subjects.add(sub);
              cboSub.insertItemAt(sub.toString(), cboSub.getItemCount()-1);
              cboSub.setSelectedIndex(cboSub.getItemCount()-2);
            } 
        } else {
            //sets subject to user's selection
            sub = subjects.get(cboSub.getSelectedIndex());
        }
        cboQuizzes.setSelectedIndex(0);
        
        //default number of questions is 10 or number within subject (whichever is lower)
        if (sub.getNumQ() > 10){
            spnNumQ.setValue(10);
        } else {
            spnNumQ.setValue(sub.getNumQ());
        }

    }//GEN-LAST:event_cboSubActionPerformed

